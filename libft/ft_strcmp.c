#include "libft.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = -1;
	if (ft_strlen(s1) != ft_strlen(s2))
		return (0);
	while (s1[++i])
		if (s1[i] != s2[i])
			return (0);
	return (1);
}
