#include "../libft.h"

int	ft_putstr(char *s)
{
	int	i;

	i = -1;
	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (s[++i])
		ft_putchar(s[i]);
	return (i);
}
